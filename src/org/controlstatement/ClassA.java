package org.controlstatement;

public class ClassA {
	
	public static void main(String[] args) {
		
		//while loop
		int a = 1;
		
		while(a<=10) {    // Entry check loop
			System.out.println(a);
			a++;	
		}
		
		System.out.println("********************");
		
		// do while 
		
		int b = 12;
		
		do {
			System.out.println(b);
			b++;
			
		} 
		
		while (b<=20);
	}

}
