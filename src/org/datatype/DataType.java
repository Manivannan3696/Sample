package org.datatype;

public class DataType {
	
	public static void main(String[] args) {
		
		byte age = 45;
		System.out.println("Age is : "+age);
		
		short regNum = 1011;
		System.out.println("Reg number : "+regNum);
		
		int empId = 11220012;
		System.out.println("Employee id : "+empId);
		
		long empMobile = 8574634973l;
		System.out.println("Employee mobile : "+empMobile);
		
		long age1 = 30;
		System.out.println("long datatype age : "+age1);
		
		// to store the decimal or fraction
		
		float productPrice = 87675.349f;
		System.out.println("product price : "+productPrice);
		
		double value = 12.1123462234;
		System.out.println("given value is : "+value);
		
		// char
		
		char gender = 'M';
		System.out.println("Gender is : "+gender);
		
		boolean answer = true ;
		System.out.println("Answer is : "+answer);
		
		String details = "Welcome to java class @OMR - 6000263";
		System.out.println("Details are given : "+details);
		
		
		
		
	}
	
	

}
